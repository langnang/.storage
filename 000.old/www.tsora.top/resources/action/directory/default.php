PHP Directory 简介
Directory 函数允许您获得关于目录及其内容的信息。

安装
PHP Directory 函数是 PHP 核心的组成部分。无需安装即可使用这些函数。

PHP 5 Directory 函数
函数	描述
chdir()	改变当前的目录。
chroot()	改变根目录。
closedir()	关闭目录句柄。
dir()	返回 Directory 类的实例。
getcwd()	返回当前工作目录。
opendir()	打开目录句柄。
readdir()	返回目录句柄中的条目。
rewinddir()	重置目录句柄。
scandir()	返回指定目录中的文件和目录的数组。
